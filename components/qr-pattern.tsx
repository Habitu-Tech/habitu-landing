export function QrPattern({ className = "" }: { className?: string }) {
  const cells = [
    [0, 0], [1, 0], [2, 0], [4, 0], [6, 0], [7, 0], [8, 0],
    [0, 1], [2, 1], [4, 1], [6, 1], [8, 1],
    [0, 2], [1, 2], [2, 2], [4, 2], [6, 2], [7, 2], [8, 2],
    [4, 3], [5, 3],
    [0, 4], [1, 4], [3, 4], [4, 4], [6, 4], [8, 4],
    [1, 5], [2, 5], [3, 5], [6, 5], [7, 5],
    [0, 6], [1, 6], [2, 6], [4, 6], [6, 6], [7, 6], [8, 6],
    [0, 7], [2, 7], [4, 7], [8, 7],
    [0, 8], [1, 8], [2, 8], [4, 8], [6, 8], [7, 8], [8, 8],
  ]

  return (
    <svg
      viewBox="0 0 90 90"
      className={className}
      aria-hidden="true"
      style={{ width: "100%", height: "100%" }}
    >
      {cells.map(([x, y], i) => (
        <rect key={i} x={x * 10} y={y * 10} width="8" height="8" rx="1.5" fill="currentColor" />
      ))}
    </svg>
  )
}
